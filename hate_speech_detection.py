import re
import emoji

def clean_text(text):
    # Step 1 - Convert to string (in case of numbers)
    text = str(text)

    # Step 2 - Remove URLs (http, www links)
    text = re.sub(r'http\S+|www\S+|https\S+', '', text)

    # Step 3 - Remove @mentions (like @username)
    text = re.sub(r'@\w+', '', text)

    # Step 4 - Remove hashtags symbol but keep word (#hate → hate)
    text = re.sub(r'#', '', text)

    # Step 5 - Remove emojis
    text = emoji.replace_emoji(text, replace='')

    # Step 6 - Remove special characters but keep
    # Indian language characters + English letters
    text = re.sub(r'[^\w\s\u0900-\u097F\u0980-\u09FF\u0C00-\u0C7F\u0C80-\u0CFF]', ' ', text)

    # Step 7 - Remove extra spaces
    text = re.sub(r'\s+', ' ', text).strip()

    # Step 8 - Remove very short texts (less than 3 characters)
    if len(text) < 3:
        return None

    return text

