def get_severity(row):
    # Not hate at all
    if row['label'] == 0:
        return 0  # clean

    # For hate speech - check length and source
    text_len = len(str(row['text_clean']))

    # Short hate text = mild
    if text_len < 50:
        return 1  # mild

    # Medium hate text = moderate
    elif text_len < 150:
        return 2  # moderate

    # Long hate text = severe
    else:
        return 3  # severe

