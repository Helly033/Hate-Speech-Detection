import torch

def predict_hate_speech(text):
    inputs = tokenizer(
        text,
        truncation=True,
        padding=True,
        max_length=128,
        return_tensors='pt'
    ).to(device)

    model.eval()
    with torch.no_grad():
        outputs = model(**inputs)
        probs   = torch.softmax(outputs.logits, dim=1)
        pred    = torch.argmax(probs, dim=1).item()
        conf    = probs[0][pred].item() * 100

    if pred == 0:
        return {
            'prediction' : 'NOT HATE SPEECH ✅',
            'severity'   : 'Clean',
            'confidence' : f'{conf:.1f}%'
        }
    else:
        if conf >= 95:
            severity = '🔴 SEVERE'
        elif conf >= 80:
            severity = '🟠 MODERATE'
        else:
            severity = '🟡 MILD'
        return {
            'prediction' : 'HATE SPEECH ❌',
            'severity'   : severity,
            'confidence' : f'{conf:.1f}%'
        }

